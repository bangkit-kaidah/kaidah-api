    public function send(Request $request) {
        $source = Source::find($request->input('id'));
        $users = $source->users;
        foreach ($users as $user) {
            $mailData = [
                'source' => $source->name,
                'receiver_name' => $user->name
            ];
            Mail::to($user)->send(new NewDocument($mailData));
        }
        return redirect('/anggota')->with('email_success' , 'Email sent successfully!');
    }
